public class Usuario {
    int id;
    String nombre;
    String correo;
    String password;
    String rol;

    public Usuario(int id, String nombre, String correo, String password, String rol) {
        this.id = id;
        this.nombre = nombre;
        this.correo = correo;
        this.password = password;
        this.rol = rol;
    }
}

